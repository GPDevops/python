print ("Operaciones aritmeticas basicas")
a = int(input("Ingrese el 1er. dato: "))
b = int(input ("Ingrese el 2do. dato: "))
c = a+b
d = a-b
e = a*b
f = a/b
g = a//b
h = a%b
i = a**b
print("El resultado de la suma es: " + str(c))
print("El resultado de la resta es: " + str(d))
print("El resultado de la multiplicacion es: " + str(e))
print("El resultado de la division es: " + str(f))
print("El resultado de la division entera es: " + str(g))
print("El resultado del modulo es: " + str(h))
print("El resultado de potencia es: " + str(i))
