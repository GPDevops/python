# dictionaries son listas en 2 dimensiones

d = {}
d["George"] = 24
d["Tom"] = 32
d["Jenny"] = 16

## print values
print(d["George"])
print(d["Tom"])
print(d["Jenny"])

for key, value in d.items():
    print("key:")
    print(key)
    print("value:")
    print(value)
    print("")

























    




    





