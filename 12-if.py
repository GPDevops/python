# Comparacion de numeros 
print("Vamos a realizar la comparacion de numeros ingresados por teclado")
numero1 = int(input("Ingrese el numero1: "))
numero2 = int(input("Ingrese el numero2: "))
if      numero1 < numero2:
        print("Numero1 es menor a Numero2")
elif    numero1 == numero2:
        print("Numero1 es igual a numero2")
elif    numero1 > numero2:
        print("Numero1 es mayor a Numero2")
print("Fin.")